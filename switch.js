const animal = "Tiger"

switch (animal) {
    case "dog":
        console.log("woof woof")
        break
    case "cat":
        console.log("meow meow")
        break
    case "Lion":
        console.log("Grr Grr")
        break
    default:
        console.log("I do not know that animal")
        break
}
